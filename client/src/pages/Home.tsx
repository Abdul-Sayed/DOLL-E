import { useState, useEffect } from "react";
import { Loader, RenderCards, FormField } from "../components";
import { PostType } from "../types";
import { backend_domain } from "../utils";

const Home = () => {
  const [posts, setPosts] = useState<PostType[]>([]);
  const [loading, setLoading] = useState<boolean>(false);
  const [searchText, setSearchText] = useState<string>("");
  const [filteredPosts, setFilteredPosts] = useState<PostType[]>([]);

  useEffect(() => {
    // Fetch all the posts
    const fetchPosts = async () => {
      setLoading(true);
      try {
        const response = await fetch(`${backend_domain}/api/posts`, {
          method: "GET",
          headers: {
            "Content-Type": "application/json",
          },
        });
        if (response.ok) {
          const posts = await response.json();
          setPosts(posts.data.reverse());
        }
      } catch (error) {
        alert(error);
      } finally {
        setLoading(false);
      }
    };
    fetchPosts();
  }, []);

  const handleSearch = (e: React.ChangeEvent<HTMLInputElement>) => {
    // Filter the posts
    const { value } = e.target;
    setSearchText(value);
    if (value) {
      const searchedResults = posts.filter(
        (post) =>
          post.name.toLowerCase().includes(value.toLowerCase()) ||
          post.prompt.toLowerCase().includes(value.toLowerCase())
      );
      setFilteredPosts(searchedResults);
    }
  };

  return (
    <section className="max-w-7xl mx-auto">
      <div className="">
        <h1 className="font-extrabold text-[32px] text-app_black">The Community Showcase</h1>
        <p className="mt-2 text-app_gray text-[16px] max-w-[500px]">
          Browse through a collection of imaginative and visually stunning images generated by
          DALL-E AI
        </p>
      </div>
      <form className="mt-12 mb-8">
        {
          <FormField
            type="text"
            labelName="Search images"
            name="text"
            value={searchText}
            placeholder="Search"
            handleChange={(e) => handleSearch(e)}
          />
        }
      </form>
      <div>
        {loading ? (
          <Loader />
        ) : (
          <>
            {searchText && (
              <h2 className="font-medium text-app_gray text-3xl mb-3">
                Showing results for <span className="text-app_black">{searchText}</span>
              </h2>
            )}
            <div className="grid grid-cols-1 lg:grid-cols-4 sm:grid-cols-3 xs:grid-cols-2 gap-3">
              {searchText ? (
                <RenderCards posts={filteredPosts} title={`No results for ${searchText}`} />
              ) : (
                <RenderCards posts={posts} title="Create some images" />
              )}
            </div>
          </>
        )}
      </div>
    </section>
  );
};

export default Home;
